                       <p>Tooth extraction also called tooth pulling. It is an Exodontics technique used by various orthodontics clinics in Delhi, to remove teeth from the mouth. It is basically used to remove unrestorable teeth, especially when they are causing toothache. In some cases wisdom teeth are unable to grow normally and may cause continuous infections.</p>
					   <h3>There are some reasons for tooth extraction:</h3>
					   <ul>
					   <li>Crowded Mouth</li>
					   <li>Infection or risk of infection</li>
					   <li>Cause of Gum Disease</li>
					   <li>When tooth decaying extends to the pulp</li>
					   <li>For proper alignment</li>
					   </ul>
					   <p>
					   Before the extraction process, local anesthesia is given by the dental experts of <strong>orthodontic clinics in Delhi </strong>to numb the gum area. For multiple tooth extraction general anesthetic is used by the dentist. If tooth is strongly impacted with the gum tissues and bone in that case dentist cut the specific gum and bone area to remove the tooth. Some times tooth must be removed or extracted in small parts. To stop the bleeding after extraction a gauze pad is used. Stitching is also an alternative of gauze pad. 
					   </p>
					   <h3>Some recovery tips by orthodontics experts are: </h3>
					   <ul>
					   <li>Do not rinse mouth vigorously</li>
					   <li>Do not smoke</li>
					   <li>Avoid drinking through the straw </li>
					   <li>Follow the diet recommended by dentist</li>
					   <li>Ignore anything that may prevent the healing like chewing gums</li>
					   <li>Ice bag or cold cloth is used for swelling</li>
					   <li>Do not brush the area near by extracted tooth</li>
